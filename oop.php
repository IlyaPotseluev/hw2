<h3>Разница между self и $this</h3>

Статические функции должны использовать только статические переменные.
<br>1) $this - это псевдоним текущего объекта внутри класса.
<br>2) self - псевдоним самого класса, внутри которого находится метод.
<br>3)self ТОЛЬКО для статических функций, свойств. Но также можно вызвать нестатический метод, 
<br>КАК СТАТИЧЕСКИЙ через self. (Но лучше так не делать)
<br>4) $this ТОЛЬКО для нестатических.
<br>5) $this требует, чтобы класс был проинициализирован, self не требует.

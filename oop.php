Наследование - механизм языка, позволяющий описать новый класс, на основе уже существующего(родительского).
<br> ласс потомок может добавить собственные методы и свойства,
<br> а также пользоваться родительскими методами и свойствами.
<hr>
Инкапсуляция - Это механизм языка, позволяющий ограничить доступ <br>
одних компонентов программы к другим.<br>
(т.е. мы решаем, какие члены класса нужны для взаимодействия, а какие скрыть)<br>

